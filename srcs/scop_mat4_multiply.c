#include "scop.h"

void	scop_mat4_multiply(t_mat4 *result, const t_mat4 a, const t_mat4 b)
{
	(*result)[0][0] = a[0][0] * b[0][0] + a[0][1] * b[1][0] +
		a[0][2] * b[2][0] + a[0][3] * b[3][0];
	(*result)[0][1] = a[0][0] * b[0][1] + a[0][1] * b[1][1] +
		a[0][2] * b[2][1] + a[0][3] * b[3][1];
	(*result)[0][2] = a[0][0] * b[0][2] + a[0][1] * b[1][2] +
		a[0][2] * b[2][2] + a[0][3] * b[3][2];
	(*result)[0][3] = a[0][0] * b[0][3] + a[0][1] * b[1][3] +
		a[0][2] * b[2][3] + a[0][3] * b[3][3];
	(*result)[1][0] = a[1][0] * b[0][0] + a[1][1] * b[1][0] +
		a[1][2] * b[2][0] + a[1][3] * b[3][0];
	(*result)[1][1] = a[1][0] * b[0][1] + a[1][1] * b[1][1] +
		a[1][2] * b[2][1] + a[1][3] * b[3][1];
	(*result)[1][2] = a[1][0] * b[0][2] + a[1][1] * b[1][2] +
		a[1][2] * b[2][2] + a[1][3] * b[3][2];
	(*result)[1][3] = a[1][0] * b[0][3] + a[1][1] * b[1][3] +
		a[1][2] * b[2][3] + a[1][3] * b[3][3];
	(*result)[2][0] = a[2][0] * b[0][0] + a[2][1] * b[1][0] +
		a[2][2] * b[2][0] + a[2][3] * b[3][0];
	(*result)[2][1] = a[2][0] * b[0][1] + a[2][1] * b[1][1] +
		a[2][2] * b[2][1] + a[2][3] * b[3][1];
	(*result)[2][2] = a[2][0] * b[0][2] + a[2][1] * b[1][2] +
		a[2][2] * b[2][2] + a[2][3] * b[3][2];
	(*result)[2][3] = a[2][0] * b[0][3] + a[2][1] * b[1][3] +
		a[2][2] * b[2][3] + a[2][3] * b[3][3];
	(*result)[3][0] = a[3][0] * b[0][0] + a[3][1] * b[1][0] +
		a[3][2] * b[2][0] + a[3][3] * b[3][0];
	(*result)[3][1] = a[3][0] * b[0][1] + a[3][1] * b[1][1] +
		a[3][2] * b[2][1] + a[3][3] * b[3][1];
	(*result)[3][2] = a[3][0] * b[0][2] + a[3][1] * b[1][2] +
		a[3][2] * b[2][2] + a[3][3] * b[3][2];
	(*result)[3][3] = a[3][0] * b[0][3] + a[3][1] * b[1][3] +
		a[3][2] * b[2][3] + a[3][3] * b[3][3];
}
